-- 1-1. STUDENT 테이블에서 학과별 평균 키
SELECT STU_DEPT, AVG(STU_HEIGHT) AVG_DEPT
FROM STUDENT
GROUP BY STU_DEPT;
-- 1-2. 학번, 이름, 학과, 키, 내가속한 학과의 평균 키
SELECT STU_NO, STU_NAME, S.STU_DEPT, STU_HEIGHT, ROUND(AVG_DEPT,1)
FROM STUDENT S
INNER JOIN (
    SELECT STU_DEPT, AVG(STU_HEIGHT) AVG_DEPT
    FROM STUDENT
    GROUP BY STU_DEPT
) T ON S.STU_DEPT = T.STU_DEPT;

-- 1-3. 내가 속한 학과의 평균 키보다 키가 큰 학번, 이름, 학과, 키
SELECT STU_NO, STU_NAME, S.STU_DEPT, STU_HEIGHT, ROUND(AVG_DEPT,1)
FROM STUDENT S
INNER JOIN (
    SELECT STU_DEPT, AVG(STU_HEIGHT) AVG_DEPT
    FROM STUDENT
    GROUP BY STU_DEPT
) T ON S.STU_DEPT = T.STU_DEPT
WHERE STU_HEIGHT > AVG_DEPT;

-- EMP, DEPT
-- 내가 속한 부서DEPT의 평균 급여보다 높은 급여를 받는 
